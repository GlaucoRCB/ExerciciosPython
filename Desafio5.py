def calculos_matematicos():
    num1 = float(input('Digite o número 1: '))
    num2 = float(input('Digite o número 2: '))

    soma = num1 + num2
    subtracao = num1 - num2
    multi = num1 * num2
    div = num1 / num2
    exp = num1 ** num2

    print(soma)
    print(subtracao)
    print(multi)
    print(div)
    print(exp)

calculos_matematicos()